int fontOffset;

void MakeRasterFont() {
	int ii;
	glPixelStorei(GL_UNPACK_ALIGNMENT, 1);
	fontOffset = glGenLists(128);
	for (ii = 32; ii < 127; ii++) {
		glNewList(ii + fontOffset, GL_COMPILE);
		glBitmap(8, 13, 0.0, 2.0, 10.0, 0.0, rasters[ii - 32]);
		glEndList();
	}
}

void CMyFontView::myinit() {
	glClear(GL_COLOR_BUFFER_BIT);
	glShadeModel(GL_FLAT);
	MakeRasterFont();
}

void CMyFontView::display() {
	void ShowString(char *s);

#ifdef _DEBUG
	static GLfloat rgb[][3] = {
		{ 0.7f, 0.7f, 0.7f }, { 0.7f, 0.7f, 0.7f }, { 0.7f, 0.7f, 0.7f },
		{ 0.7f, 0.7f, 0.7f }, { 0.7f, 0.7f, 0.7f }, { 0.7f, 0.7f, 0.7f },
		{ 0.7f, 0.7f, 0.7f }
	};
#else
	static GLfloat rgb[][3] = {
		{ 0.2f, 0.5f, 0.5f }, { 0.4f, 0.5f, 0.5f }, { 0.6f, 0.5f, 0.5f },
		{ 0.8f, 0.5f, 0.5f }, { 1.0f, 0.5f, 0.5f }, { 1.0f, 0.3f, 0.3f }
	};
#endif

	int ii, jj;
	char teststring[100];
	myinit();
	for (ii = 16; ii < 127; ii += 16) {
		glRasterPos2i(20, 300 - 18 * ii / 16);
		glColor3fv(rgb[ii / 16 - 1]);
		for (jj = 0; jj < 16; jj++) {
			teststring[jj] = (char)(ii + jj);
		}
		teststring[16] = '\0';
		ShowString(teststring);
	}
	glRasterPos2i(20, 100);
	ShowString("OpenGL:: A Whole New World");
	glFlush();
}

void ShowString(char *s) {
	glPushAttrib(GL_LIST_BIT);
	glListBase(fontOffset);
	glCallLists(strlen(s), GL_UNSIGNED_BYTE, (GLubyte *)s);
	glPopAttrib();
}
