TRetStr GetCloseNum(char *str, Word p1){
  char *Num;
  Word count=0;
  _TRetStr.r=NULL;
  _TRetStr.s=1;
  while ((str[p1+count]>='0')&&(str[p1+count]<='9')) count++;
  Num=new char[count+1];
  Num[count]=0;
  while (count) Num[count-1]=str[p1+count---1];
  _TRetStr.r=Num;
  delete Num;
  return _TRetStr;
}
